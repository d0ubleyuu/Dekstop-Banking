    public void history(){
        Object [] baris = {"Tanggal","Waktu","Jenis Transaksi","Debit","Kredit","Saldo","Keterangan"};
        tabmode = new DefaultTableModel(null, baris);
        tbl_history.setModel(tabmode);
        
        try {
                rs = aa.ambilDataTransaksi(); 
                while (rs.next()){
                    aa.setTgl(rs.getString("tanggal"));
                    aa.setWaktu(rs.getString("waktu"));
                    aa.setJk(rs.getString("jenis_transaksi"));
                    aa.setDebit(rs.getString("debit"));
                    aa.setKredit(rs.getString("kredit"));
                    aa.setSaldoT(rs.getString("saldo"));
                    aa.setKet(rs.getString("ket"));
                    
                    String[] data = {aa.getTgl(),aa.getWaktu(),aa.getJk(),aa.getDebit(),aa.getKredit(),aa.getSaldoT(),aa.getKet()};
                    tabmode.addRow(data);
                }
        } catch (Exception e) {
                System.out.print("Gagal Memuat Daftar Transaksi!"+"\n"+e);
        }
    }
