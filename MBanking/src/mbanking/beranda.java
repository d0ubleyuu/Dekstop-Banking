    private void btn_transaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_transaksiActionPerformed
        // TODO add your handling code here:
        if (txtNamaTransfer.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Masukkan Nomor Rekening Penerima");
        } else if (txtNominlTransaksi.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Masukkan Nominal Transfer");
        } else {
            aa.setTgl(lbl_date.getText());
            aa.setWaktu(lbl_time.getText());
            aa.setJk("Transfer");
            aa.setDebit("0");
            aa.setKredit(txtNominlTransaksi.getText());
            aa.setKet(aa.getNamaPenerima());
            rs = aa.ambilData();

            try {
                rs.next();
                System.out.println(aa.getSaldoTarik(rs.getString("saldo")));
                if(Integer.parseInt(txtNominlTransaksi.getText()) >= 50000 ){
                    if(Integer.parseInt(rs.getString("saldo")) >= Integer.parseInt(txtNominlTransaksi.getText())){
                        System.err.println(rs.getString("saldo"));
                        if(Integer.parseInt(JOptionPane.showInputDialog("Masukkan Pin Anda")) == Integer.parseInt(rs.getString("pin"))){
                            System.err.println(rs.getString("pin"));
                            aa.Transaksi();
                            JOptionPane.showMessageDialog(null, "Setor Tunai Berhasil", "Succesfull", JOptionPane.INFORMATION_MESSAGE);
                            tabPane.setSelectedIndex(1);
                            aa.ubahSaldo();
                            aa.ubahSaldoPen();
                            check();
                            history();
                            clear();
                        } 
                    } else {
                        JOptionPane.showMessageDialog(null,  "Maaf Saldo Anda tidak Mencukupi untuk melakukan Transfer", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Minimal Transfer Rp. 50.000,-");
                }
            } catch (SQLException ex) {
                System.out.println(ex); 
            }
        }
    }//GEN-LAST:event_btn_transaksiActionPerformed

